package com.kodilla.basic_assertion;

public class Calculator {


    public int sum(int a, int b){
        return a+b;
    }
    public int substract(int a, int b){
        return a-b;
    }
    public double exponentiation(int potega, double ab){
        double result =Math.pow(ab,potega);
        return result;
    }

}
